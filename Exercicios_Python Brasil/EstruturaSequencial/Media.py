#Faça um Programa que peça as 4 notas bimestrais e mostre a média.


#Entrada de dados
Pnota=int(input('Digite sua primeira nota:'))
Snota=int(input('Digite sua segunda nota:'))
Tnota=int(input('Digite sua terceira nota:'))
Qnota=int(input('Digite sua quarta nota:'))

#soma dos dados 

Media = int((Pnota+Snota+Tnota+Qnota)/4)

#Saida de dadps

print("Sua média é:",Media)

