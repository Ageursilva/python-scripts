#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

print('Esse prorama vai te ajudar a calcular a área de um quadrado :)')
#Entrada de dados
lado=int(input("Quando mede os lados do seu quadrado? "))

#Calculo a area (A=l² ou A=a.b)

Area=lado**2

#saida de dados

print('A área de seu quadrado é de', Area)
print('O dobro da área é de', Area*2)

