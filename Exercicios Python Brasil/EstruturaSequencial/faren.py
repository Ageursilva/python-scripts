#Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Farenheit.
print('Esse programa vai conveter ºC para ºF')

#entrada de dados

Celsi=float(input("Digite o valo que deseja converter: "))

#Conversaõ
faren = (Celsi*9/5)+32

#saida de dados

print("O valor é",faren,"ºF")


#(10 °C × 9/5) + 32 = 50 °F
